package br.com.delphos.excecoes;

import br.com.delphos.sca.constantes.CodigoMensagem;

public class ValidacaoException extends DelphosException {
	private static final long serialVersionUID = 1L;
	
	public ValidacaoException() {
		super();
	}

	public ValidacaoException(CodigoMensagem codigoRetorno, Object... args) {
		super(codigoRetorno, args);
	}

	public ValidacaoException(String m, Throwable t) {
		super(m, t);
	}

	public ValidacaoException(String m) {
		super(m);
	}

	public ValidacaoException(Throwable t) {
		super(t);
	}
	
}
